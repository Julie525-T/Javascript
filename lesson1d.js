// Data types


// Integer
let age=25
console.log(age);
console.log(typeof(age));

// float

let weight=20.5
console.log(weight);
console.log(typeof(weight));

// string

let username="Julie"
console.log(username);
console.log(typeof(username));

// Boolean

let isAdmin=true
let isRegistrered=false

console.log(isAdmin);
console.log(typeof(isAdmin));
console.log(typeof(isRegistrered));

// Undefined

let somevariable;
console.log(somevariable);
console.log(typeof(somevariable));


// null

let user=null
console.log(user)
console.log(typeof(user))
